	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	public float GetZoomScale()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( WorldMap()!=(null) && WorldMap().Camera()!=(null) )
		{
			return Func.Divide( WorldMap().Camera().GetFieldOfView(), (SYSTEM.ZOOM_MAX) );
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (1f);
	}

