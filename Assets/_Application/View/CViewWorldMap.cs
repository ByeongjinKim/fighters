	//-------------------------------------------------------------------------------------------------------------------------------
	// 도시 정보를 등록하기 위한 함수
	//-------------------------------------------------------------------------------------------------------------------------------
	public uiWorldMapCityPin Register( Vector2 vPos, tagCityStatic citystatic )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (citystatic)==(null) ) return (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		uiWorldMapCityPin pin = (Func.Create( _Transform(), (Resources.Load("UI/WorldMapCityPin") as GameObject) ).GetComponent(typeof(uiWorldMapCityPin)) as uiWorldMapCityPin);
		if( (transform)==(null) ) return (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(pin.Transform().position)			= Func.WorldToCanvasPoint( WorldMap().Camera(), Canvas(), new Vector2( (vPos.x)*(WorldMap().Transform().localScale.x), (vPos.y)*(WorldMap().Transform().localScale.y) ) );
		(pin.Transform().localEulerAngles)	= new Vector3();
		(pin.Transform().localScale)		= new Vector3( (0.035f), (0.035f), (1f) );

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		(citystatic.pin)	= (pin);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
//		app.CityStatic.CorverTextureDownload( (citystatic), (pin.funcTextureDownload) );
		pin.Set( citystatic );

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		GetList().Add( pin );

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		return (pin);
	}

	//-------------------------------------------------------------------------------------------------------------------------------
	// -
	//-------------------------------------------------------------------------------------------------------------------------------
	void Sort()
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		int					End		= (GetList().Count);
		uiWorldMapCityPin	temp	= (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		for( int i=0; i<(End-1); i++ )
		{
			for( int j=(i+1); j<(End); j++ )
			{
				if( (GetList()[i].Transform().localPosition.y)<(GetList()[j].Transform().localPosition.y) )
				{
					(temp)			= GetList()[i];
					GetList()[i]	= GetList()[j];
					GetList()[j]	= (temp);
				}
			}
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		for( int i=0; i<(End); i++ )
		{
			GetList()[i].Transform().SetSiblingIndex(i);
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
	}

