		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( (product)==(null) ) return;

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		Func.Destroy( product._GameObject() );
		Products().Remove( product );
		Refresh();

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( ProductLayer()!=(null) )
		{
			(Transform().sizeDelta)	= new Vector2( (Transform().sizeDelta.x), (Transform().sizeDelta.y-ProductLayer().sizeDelta.y) );
		}

		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
