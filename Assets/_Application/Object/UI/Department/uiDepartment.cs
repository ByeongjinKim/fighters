	public uiProduct Find( string id )
	{
		//---------------------------------------------------------------------------------------------------------------------------
		// -
		//---------------------------------------------------------------------------------------------------------------------------
		if( !Func.Is(id) ) return (null);

		//---------------------------------------------------------------------------------------------------------------------------
		// -
